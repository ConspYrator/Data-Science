"""
Mapper function printing out 10 lines containing longest posts, sorted in
ascending order from shortest to longest.
"""
import sys
import csv

def mapper():
    reader = csv.reader(sys.stdin, delimiter='\t')
    writer = csv.writer(sys.stdout, delimiter='\t', quotechar='"', quoting=csv.QUOTE_ALL)
    list = []
    for line in reader:
        length = len(line[4])
        list.append((length, line))
    list.sort()
    for x in list[-10:]:
        writer.writerow(x[1])



